class AdminPage extends StatefulWidget {
  const AdminPage({super.key});
  @override
  State<AdminPage> createState() => _AdminPageState();
}

class _AdminPageState extends State<AdminPage> {
  String _keyword = '';
  bool _sortByAvgTime = false; // false: Ï†êÏàò ÎÇ¥Î¶ºÏ∞®Ïàú, true: ÌèâÍ∑†ÏãúÍ∞Ñ Ïò§Î¶ÑÏ∞®Ïàú
  bool _exporting = false;
  bool _deleting = false;

  Future<void> _exportCsv(
    List<QueryDocumentSnapshot<Map<String, dynamic>>> docs,
  ) async {
    if (docs.isEmpty) {
      if (!mounted) return;
      ScaffoldMessenger.of(context).showSnackBar(
        const SnackBar(content: Text('ÎÇ¥Î≥¥ÎÇº Îç∞Ïù¥ÌÑ∞Í∞Ä ÏóÜÏäµÎãàÎã§.')),
      );
      return;
    }

    setState(() => _exporting = true);
    try {
      final rows = <List<dynamic>>[
        ['name', 'code', 'totalScore', 'avgTimeMs', 'count', 'updatedAt', 'profileId'],
        ...docs.map((d) {
          final m = d.data();
          final name = (m['name'] ?? '').toString();
          final code = (m['code'] ?? '').toString();
          final pid  = makeProfileId(name, code);
          return [
            name,
            code,
            m['totalScore'] ?? 0,
            m['avgTimeMs'] ?? '',
            m['count'] ?? '',
            (m['updatedAt'] is Timestamp)
                ? (m['updatedAt'] as Timestamp).toDate().toIso8601String()
                : '',
            pid,
          ];
        }),
      ];

      final csv = const ListToCsvConverter().convert(rows);
      final bytes = Uint8List.fromList(csv.codeUnits);

      await FileSaver.instance.saveFile(
        name: 'MCI_users_${DateTime.now().toIso8601String()}',
        bytes: bytes,
        ext: 'csv',
        mimeType: MimeType.csv,
      );

      if (!mounted) return;
      ScaffoldMessenger.of(context)
          .showSnackBar(const SnackBar(content: Text('CSV Ï†ÄÏû• ÏôÑÎ£å')));
    } catch (e) {
      if (!mounted) return;
      ScaffoldMessenger.of(context)
          .showSnackBar(SnackBar(content: Text('CSV Ï†ÄÏû• Ïã§Ìå®: $e')));
    } finally {
      if (mounted) setState(() => _exporting = false);
    }
  }

  Future<void> _deleteUserCascade(String profileId, String displayName, String code) async {
    if (_deleting) return;
    final ok = await showDialog<bool>(
      context: context,
      builder: (_) => AlertDialog(
        title: const Text('ÎåÄÏõê ÏÇ≠Ï†ú'),
        content: Text('[$displayName ($code)] ÎåÄÏõêÏùò Î™®Îì† Í∏∞Î°ùÏùÑ ÏÇ≠Ï†úÌï†ÍπåÏöî?\n(ÏÇ¨Ïö©Ïûê Î¨∏ÏÑúÏôÄ Î™®Îì† ÏÑ∏ÏÖòÏù¥ ÏÇ≠Ï†úÎê©ÎãàÎã§)'),
        actions: [
          TextButton(onPressed: () => Navigator.pop(context, false), child: const Text('Ï∑®ÏÜå')),
          FilledButton.tonal(onPressed: () => Navigator.pop(context, true), child: const Text('ÏÇ≠Ï†ú')),
        ],
      ),
    );
    if (ok != true) return;

    setState(() => _deleting = true);
    try {
      final userRef = FirebaseFirestore.instance.collection('users').doc(profileId);
      final qs = await userRef.collection('sessions').get();
      // ÌïòÏúÑ ÏÑ∏ÏÖò Î™®Îëê ÏÇ≠Ï†ú
      for (final doc in qs.docs) {
        await doc.reference.delete();
      }
      // ÏÇ¨Ïö©Ïûê Î¨∏ÏÑú ÏÇ≠Ï†ú
      await userRef.delete();

      if (!mounted) return;
      ScaffoldMessenger.of(context).showSnackBar(
        const SnackBar(content: Text('ÎåÄÏõêÏùÑ ÏÇ≠Ï†úÌñàÏäµÎãàÎã§.')),
      );
    } catch (e) {
      if (!mounted) return;
      ScaffoldMessenger.of(context).showSnackBar(
        SnackBar(content: Text('ÏÇ≠Ï†ú Ïã§Ìå®: $e')),
      );
    } finally {
      if (mounted) setState(() => _deleting = false);
    }
  }

  @override
  Widget build(BuildContext context) {
    final col = FirebaseFirestore.instance.collection('users');
    final query = _sortByAvgTime
        ? col.orderBy('avgTimeMs').limit(500)
        : col.orderBy('totalScore', descending: true).limit(500);

    return Scaffold(
      appBar: AppBar(title: const Text('Í¥ÄÎ¶¨Ïûê ÌéòÏù¥ÏßÄ')),
      body: Padding(
        padding: const EdgeInsets.all(16),
        child: Column(
          children: [
            // üîé ÌïÑÌÑ∞ & Ï†ïÎ†¨
            Row(
              children: [
                Expanded(
                  child: TextField(
                    decoration: const InputDecoration(
                      prefixIcon: Icon(Icons.search),
                      hintText: 'Ïù¥Î¶Ñ ÎòêÎäî ÏΩîÎìú Í≤ÄÏÉâ',
                    ),
                    onChanged: (v) => setState(() => _keyword = v.trim()),
                  ),
                ),
                const SizedBox(width: 12),
                FilterChip(
                  label: const Text('ÌèâÍ∑†ÏãúÍ∞Ñ Í∏∞Ï§Ä'),
                  selected: _sortByAvgTime,
                  onSelected: (v) => setState(() => _sortByAvgTime = v),
                ),
              ],
            ),
            const SizedBox(height: 12),

            // üìä Î™©Î°ù
            Expanded(
              child: StreamBuilder<QuerySnapshot<Map<String, dynamic>>>(
                stream: query.snapshots(),
                builder: (context, snap) {
                  if (snap.hasError) {
                    return Center(child: Text('Ïò§Î•ò: ${snap.error}'));
                  }
                  if (!snap.hasData) {
                    return const Center(child: CircularProgressIndicator());
                  }

                  final all = snap.data!.docs;

                  // ÌÇ§ÏõåÎìú ÌïÑÌÑ∞
                  final filtered = all.where((d) {
                    if (_keyword.isEmpty) return true;
                    final m = d.data();
                    final name = (m['name'] ?? '').toString();
                    final code = (m['code'] ?? '').toString();
                    final kw = _keyword.toLowerCase();
                    return name.toLowerCase().contains(kw) ||
                        code.toLowerCase().contains(kw);
                  }).toList();

                  // Í∞ÑÎã® ÏßëÍ≥Ñ
                  final totalUsers = filtered.length;
                  final sumScore = filtered.fold<int>(
                      0, (a, d) => a + ((d.data()['totalScore'] ?? 0) as int));
                  final avgOfAvgTimeMs = filtered.isEmpty
                      ? 0
                      : (filtered.fold<int>(
                                  0,
                                  (a, d) =>
                                      a + ((d.data()['avgTimeMs'] ?? 0) as int)) /
                              filtered.length)
                          .round();

                  return Column(
                    children: [
                      Row(
                        children: [
                          Text('Ï¥ù $totalUsersÎ™Ö',
                              style: Theme.of(context).textTheme.titleMedium),
                          const SizedBox(width: 12),
                          Expanded(
                            child: Text(
                              _sortByAvgTime
                                  ? 'ÌèâÍ∑†ÏãúÍ∞Ñ(ÌèâÍ∑†): ${avgOfAvgTimeMs > 0 ? (avgOfAvgTimeMs / 1000).toStringAsFixed(1) : "-"}Ï¥à'
                                  : 'ÎàÑÏ†ÅÏ†êÏàò Ìï©Í≥Ñ: $sumScore',
                              style: const TextStyle(color: Colors.black54),
                            ),
                          ),
                          const SizedBox(width: 12),
                          OutlinedButton.icon(
                            onPressed:
                                _exporting ? null : () => _exportCsv(filtered),
                            icon: _exporting
                                ? const SizedBox(
                                    width: 16,
                                    height: 16,
                                    child:
                                        CircularProgressIndicator(strokeWidth: 2),
                                  )
                                : const Icon(Icons.download),
                            label: const Text('CSV ÎÇ¥Î≥¥ÎÇ¥Í∏∞'),
                          ),
                        ],
                      ),
                      const SizedBox(height: 8),
                      Expanded(
                        child: Card(
                          elevation: 0,
                          shape: RoundedRectangleBorder(
                            borderRadius: BorderRadius.circular(12),
                            side: const BorderSide(color: Colors.black12),
                          ),
                          child: ListView.separated(
                            itemCount: filtered.length,
                            separatorBuilder: (context, index) =>
                                const Divider(height: 1),
                            itemBuilder: (context, i) {
                              final d = filtered[i];
                              final m = d.data();
                              final name = (m['name'] ?? 'Î¨¥Î™Ö') as String;
                              final code = (m['code'] ?? '') as String;
                              final score = (m['totalScore'] ?? 0) as int;
                              final avgMs = (m['avgTimeMs'] ?? 0) as int?;
                              final count = (m['count'] ?? 0) as int?;
                              final pid = makeProfileId(name, code);

                              return ListTile(
                                leading: Text('${i + 1}',
                                    style: const TextStyle(
                                        fontWeight: FontWeight.w600)),
                                title: Text('$name (${code.isNotEmpty ? code : "--"})'),
                                subtitle: Text(
                                  _sortByAvgTime
                                      ? 'ÌèâÍ∑†ÏãúÍ∞Ñ: ${avgMs != null && avgMs > 0 ? (avgMs / 1000).toStringAsFixed(1) : "-"}Ï¥à'
                                      : 'ÎàÑÏ†ÅÏ†êÏàò: $score ‚Ä¢ ÏÑ∏ÏÖò ${count ?? 0}Ìöå',
                                ),
                                trailing: Row(
                                  mainAxisSize: MainAxisSize.min,
                                  children: [
                                    // ÏÇ≠Ï†ú Î≤ÑÌäº
                                    IconButton(
                                      tooltip: 'ÎåÄÏõê ÏÇ≠Ï†ú',
                                      icon: _deleting
                                          ? const SizedBox(
                                              width: 18,
                                              height: 18,
                                              child: CircularProgressIndicator(strokeWidth: 2),
                                            )
                                          : const Icon(Icons.delete_outline),
                                      onPressed: _deleting
                                          ? null
                                          : () => _deleteUserCascade(pid, name, code),
                                    ),
                                    const Icon(Icons.chevron_right),
                                  ],
                                ),
                                onTap: () {
                                  Navigator.push(
                                    context,
                                    MaterialPageRoute(
                                      builder: (context) => AdminUserDetailPage(
                                        profileId: pid,
                                        displayName: name,
                                        displayCode: code,
                                      ),
                                    ),
                                  );
                                },
                              );
                            },
                          ),
                        ),
                      ),
                    ],
                  );
                },
              ),
            ),
          ],
        ),
      ),
    );
  }
}
